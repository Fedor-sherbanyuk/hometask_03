function squareArea(sideLengthSquare) {
    return sideLengthSquare * sideLengthSquare;
}

function squareRectangle(firstSideOfTheRectangle, secondSideOfTheRectangle) {
    return firstSideOfTheRectangle * secondSideOfTheRectangle;
}

function squareParallelogram(sideLengthParallelogram, heightLengthParallelogram) {
    return sideLengthParallelogram * heightLengthParallelogram;
}

function squareRhombus(rhombusSideLength, rhombusHeightLength) {
    return rhombusSideLength * rhombusHeightLength;
}